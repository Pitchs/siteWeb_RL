<p>
Dans le cadre du BTS SIO, la mise en place d’une veille technologique est imposée.
La veille permet de s’informer des nouveautés dans certains domaines définis.
</p>
<p>
Dans le cadre de ma veille technologique j’ai choisi de sélectionner comme thème la « L'ordinateur Quantique ».
Aillant conscience que l'ordinateur binaire est proche de ses limites en termes de d'optimisation des composants...
J'ai trouvé intéressant de suivre l'évolution de la technologie qui pourrais surpasser de loin ce que nous connaissons a l'heure actuelle.
</p>
<p>
J'ai donc commencé à faire des recherches "en vrac", en sauvegardant dans mes marques pages, fichier texte avec des liens url sur les divers sujets de la technologie quantique.
Après avoir perdu plusieurs sources, m'être rendu compte des limites de cette méthodes et ayant eu une formation sur les méthodes de veilles.
J'ai créé un compte NetVibes afin d'optimiser mon organisation.
</p>
